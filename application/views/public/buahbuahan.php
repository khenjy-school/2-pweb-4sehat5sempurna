            <h1>Buah-Buahan</h1>
            <p>Author : Ehren</p>
            <p>Buah adalah pertumbuhan sempurna dari bakal buah. Buah juga termasuk sebagai sumber mineral, vitamin, dan serat yang baik untuk tubuh.</p>
            <p>Manfaat buah berdasarkan warna :</p>

            <p>Buah Warna Hijau</p>
            <p>=> Buah berwarna hijau mengandung vitamin C, beta karoten, folat dan kalsium.
            Fungsi dari vitamin yang terkandung pada buah berwarna hijau adalah menurunkan kolesterol, mendukung kesehatan mata, menormalkan waktu percernaan dan melawan radikal bebas.</p>
            <p>Contoh buah : Kiwi, Apel Hijau, dan Melon.</p>

            <p>Buah Warna Kuning/Oranye</p>
            <p>=> Buah berwarna oranye mengandung beta karoten, kalium dan vitamin C. Kalium pada buah berfungsi menurunkan tekanan darah tinggi dan melancarkan metabolisme tubuh.</p>
            Buah berwarna kuning/oranye juga efektif melawan kanker.</p>
            <p>Contoh buah : Jeruk, Pisang, dan Mangga</p>

            <p>Buah Warna Ungu</p>
            <p>=> Buah berwana ungu mengandung magnesium untuk melancarkan pencernaan dan memurunkan kadar 
            kolesterol.
            Buah berwarna ungu meningkatkan sistem kekebalan tubuh, dan mencegah peradangan.</p>
            <p>Contoh buah : Anggur, Bluberi, dan Plum</p>

            <p>Buah Warna Merah</p>
            <p>=> Buah berwarna merah mengandung asam ellagic dan hesperidin yang berfungsi mencegah kanker serta 
            menghambat radikal bebas.
            Contoh buah : Semangka, Stroberi, dan Tomat.</p>


