            <h1>Sayuran</h1>
            <p>Author : Efrend</p>
            <p>Jenis-Jenis Sayuran</p>
            <p>Sayuran merupakan sebutan umum bagi  bahan pangan asal tumbuhan yang biasanya mengandung kadar air tinggi dan dikonsumsi dalam keadaan segar atau setelah diolah secara minimal. Sebutan untuk beraneka jenis sayuran disebut sebagai  sayur-sayuran atau sayur-mayur. Sejumlah sayuran dapat dikonsumsi mentah tanpa dimasak terlebih dahulu, sementara yang lainnya harus diolah terlebih dahulu  dengan cara direbus, dikukus, digoreng, disangrai, atau dimakan mentah-mentah.  Sayuran berbentuk daun yang dimakan mentah disebut sebagai lalapan</p>

            <p>Brokoli</p>

            <p>Brokoli (Brassica oleracea var, botrytis) dikenal sebagai sayuran sub-tropis yang termasuk anggota keluarga kubis-kubisan (Cruciferae). Yang dikonsumsi adalah bunga (curd) yang berwarna hijau gelap atau hijau kebiru-biruan, disebut, sprouting broccoli</p>

            <p>Bunganya yang hijau pekat ini mempunyai bau yang khas, disebabkan adanya senyawa indol. Sebuah teori mengatakan, di dalam tubuh senyawa ini dapat meningkatkan ekskresi estrogen, senyawa yang dapat merangsang pembiakan sel kanker yang bersarang dalam rahim dan payudara.</p>

            <p>Seledri</p>

            <p>Seledri (Apium graveolens) termasuk famili Umbeliflorae. Seledri di dapur rumah tangga digunakan sebagai pelengkap berbagai masakan, sebab selain bentuk daunnya yang cantik juga baunya pemberi harum pada masakan -baik batang maupun daunnya.</p>

            <p>Sayuran yang dikenal rendah kalori dan sarat serat kasar ini juga merupakan sumber Vitamin A, B, dan C, terutama pada batangnya. Kandungan mineralnya pun cukup banyak. Fungsi lainnya ialah dapat menurunkan tekanan darah tinggi. Seledri ini pun sering digunakan sebagai obat kesehatan rambut.</p>

            <p>Wortel</p>

            <p>Sayuran yang mempunyai nama latin Daucus carota ini terkenal sebagai sumber Vitamin A karena mengandung Provitamin A
            Vitamin A sangat baik untuk menjaga kesehatan mata dan meningkatkan ketahanan tubuh terhadap penyakit. Beta-karoten yang merupakan senyawa Provitamin A mampu mencegah reaktivitas bensopiren penyebab kanker paru-paru.</p>

